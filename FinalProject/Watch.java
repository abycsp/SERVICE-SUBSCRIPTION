    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String plan = secretcode.getText();
        
        if(plan.equals("BNF01") || plan.equals("SNF02") || plan.equals("PNF03")){
            
            
            logopanel.setBackground(Color.black);
            logo.setText("NETFLIX");
            logo.setFont(new Font("Arial", Font.BOLD, 28));
            logo.setForeground(Color.red);
            
        }
        
        else if(plan.equals("HBOP1") || plan.equals("HBOP2") || plan.equals("HBOP3")){
            
            
            
            logopanel.setBackground(Color.black);
            logo.setText("HBO");
            logo.setFont(new Font("Berlin Sans FB", Font.BOLD, 28));
            logo.setForeground(Color.white);
            
        }
        
        else if(plan.equals("ABPM480") || plan.equals("ABPM720") || plan.equals("APMG1K")){
            
            
            logopanel.setBackground(new Color(255,153,0));
            logo.setText("amazon prime videos");
            logo.setFont(new Font("Arial Rounded MT Bold", Font.BOLD, 28));
            logo.setForeground(Color.black);
            
        }
        
        else {
            JOptionPane.showMessageDialog(null, "Please Enter a Valid Code!");
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

